static void
print_buf(unsigned char *buf, size_t len)
{
	int		 i;

	for (i = 0; i  < len; i++) {
		if (i % 8 == 0)
			printf("\n\t");
		printf("%02hx ", buf[i]);
	}
	printf("\n");
}


